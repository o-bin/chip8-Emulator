const std::uint8_t* BitmapFont::Glyph(char c) const {
  if (c >= 'a' && c <= 'z') c = static_cast<char>(c - 'a' + 'A');
  if (c < 0x20 || c > 0x7F) c = ' ';
  return kFont[static_cast<std::uint8_t>(c - 0x20)];
}

int BitmapFont::MeasureWidth(std::string_view text, std::uint32_t scale) const {
  return static_cast<int>(text.size() * 8 * scale);
}

void BitmapFont::DrawText(SDL_Renderer* r, int x, int y, std::string_view text, std::uint32_t scale) const {
  SDL_SetRenderDrawColor(r, 235, 235, 235, 255);
  int cx = x;
  for (char c : text) {
    const std::uint8_t* g = Glyph(c);
    for (int row = 0; row < 8; row++) {
      const std::uint8_t bits = g[row];
      for (int col = 0; col < 8; col++) {
        if (bits & (1u << (7 - col))) {
          SDL_Rect px{cx + static_cast<int>(col * scale), y + static_cast<int>(row * scale),
                      static_cast<int>(scale), static_cast<int>(scale)};
          SDL_RenderFillRect(r, &px);
        }
      }
    }
    cx += static_cast<int>(8 * scale);
  }
}

